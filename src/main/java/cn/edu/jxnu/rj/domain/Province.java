package	cn.edu.jxnu.rj.domain;
public class Province {
	private int id;
	private String province;
	public void setId(int id){
		this.id = id;
	}
	public int getId(){
		return id;
	}
	public void setProvince(String province){
		this.province = province;
	}
	public String getProvince(){
		return province;
	}
}
